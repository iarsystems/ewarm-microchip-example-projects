//------------------------------------------------------------------------------
//         Internal constants
//------------------------------------------------------------------------------

#if defined(PINS_DDRAM)
#undef IMAGE_BUF_BASE_ADDR
#define IMAGE_BUF_BASE_ADDR AT91C_DDR2
#endif

#if defined(PINS_SDRAM)
#undef IMAGE_BUF_BASE_ADDR
#define IMAGE_BUF_BASE_ADDR AT91C_EBI_SDRAM
#endif

/// Custom INFO trace level for the current file.
#define INFO                4

/// Maximum number of images in slideshow.
#define MAXIMAGES           10

/// Base address for storing BMP files.
#define FIRSTIMAGEADDRESS   (IMAGE_BUF_BASE_ADDR + 0x00200000)

/// Missing RTTC0 definition.
#if !defined(AT91C_BASE_RTTC) && defined(AT91C_BASE_RTTC0)
    #define AT91C_BASE_RTTC    AT91C_BASE_RTTC0
#endif

//------------------------------------------------------------------------------
//         Prototypes
//------------------------------------------------------------------------------

static void ReceiveBmpHeader();
static void ReceiveBmpData(unsigned int size);

//------------------------------------------------------------------------------
//         Internal constants
//------------------------------------------------------------------------------

/// Primary display buffer.
unsigned char *primaryBuffer = (unsigned char *) (IMAGE_BUF_BASE_ADDR + 0x00010000);

/// Back buffer (for double-buffering).
unsigned char *backBuffer = (unsigned char *) (IMAGE_BUF_BASE_ADDR + 0x00100000);

/// Number of received images (for slideshow).
unsigned char numImages;

/// Address of BMP files to display.
unsigned int images[MAXIMAGES];

/// Size of BMP files;
unsigned int imageSizes[MAXIMAGES];

//------------------------------------------------------------------------------
//         VBus monitoring (optional)
//------------------------------------------------------------------------------
#if defined(PIN_USB_VBUS)

#define VBUS_CONFIGURE()  VBus_Configure()

/// VBus pin instance.
static const Pin pinVbus = PIN_USB_VBUS;

//------------------------------------------------------------------------------
/// Handles interrupts coming from PIO controllers.
//------------------------------------------------------------------------------
static void ISR_Vbus(const Pin *pPin)
{
    // Check current level on VBus
    if (PIO_Get(&pinVbus)) {

        TRACE_INFO("VBUS conn\n\r");
        USBD_Connect();
    }
    else {

        TRACE_INFO("VBUS discon\n\r");
        USBD_Disconnect();
    }
}

//------------------------------------------------------------------------------
/// Configures the VBus pin to trigger an interrupt when the level on that pin
/// changes.
//------------------------------------------------------------------------------
static void VBus_Configure( void )
{
    TRACE_INFO("VBus configuration\n\r");

    // Configure PIO
    PIO_Configure(&pinVbus, 1);
    PIO_ConfigureIt(&pinVbus, ISR_Vbus);
    PIO_EnableIt(&pinVbus);

    // Check current level on VBus
    if (PIO_Get(&pinVbus)) {

        // if VBUS present, force the connect
        TRACE_INFO("conn\n\r");
        USBD_Connect();
    }
    else {
        USBD_Disconnect();
    }           
}

#else
    #define VBUS_CONFIGURE()    USBD_Connect()
#endif //#if defined(PIN_USB_VBUS)

