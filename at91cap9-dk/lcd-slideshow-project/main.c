//------------------------------------------------------------------------------
/// Configures the PIO needed by the application.
//------------------------------------------------------------------------------
static void ConfigurePins()
{
#if defined(PINS_SDRAM)
    static const Pin pins[] = {PINS_LCD, PINS_SDRAM};
#elif defined(PINS_DDRAM)
     static const Pin pins[] = {PINS_LCD, PINS_DDRAM};
#else
    #error no PINS_SDRAM or PINS_DDRAM
#endif

    PIO_Configure(pins, PIO_LISTSIZE(pins));
}

//------------------------------------------------------------------------------
/// Initializes the LCD controller with the board parameters.
//------------------------------------------------------------------------------
void InitializeLcd()
{
    // Enable peripheral clock
    AT91C_BASE_PMC->PMC_PCER = 1 << AT91C_ID_LCDC;

#if defined(at91sam9g10)||defined(at91sam9261)
    AT91C_BASE_PMC->PMC_SCER = AT91C_PMC_HCK1;
#endif

    // Disable the LCD and the DMA
    LCD_DisableDma();
    LCD_Disable(0);

    // Configure the LCD controller
    LCD_SetPixelClock(BOARD_MCK, BOARD_LCD_PIXELCLOCK);
    LCD_SetDisplayType(BOARD_LCD_DISPLAYTYPE);
    LCD_SetScanMode(AT91C_LCDC_SCANMOD_SINGLESCAN);
    LCD_SetBitsPerPixel(BOARD_LCD_BPP);
    LCD_SetPolarities(BOARD_LCD_POLARITY_INVVD,
                      BOARD_LCD_POLARITY_INVFRAME,
                      BOARD_LCD_POLARITY_INVLINE,
                      BOARD_LCD_POLARITY_INVCLK,
                      BOARD_LCD_POLARITY_INVDVAL);
    LCD_SetClockMode(BOARD_LCD_CLOCKMODE);
    LCD_SetMemoryFormat((unsigned int) AT91C_LCDC_MEMOR_LITTLEIND);
    LCD_SetSize(BOARD_LCD_WIDTH, BOARD_LCD_HEIGHT);

    // Configure timings
    LCD_SetVerticalTimings(BOARD_LCD_TIMING_VFP,
                           BOARD_LCD_TIMING_VBP,
                           BOARD_LCD_TIMING_VPW,
                           BOARD_LCD_TIMING_VHDLY);
    LCD_SetHorizontalTimings(BOARD_LCD_TIMING_HBP,
                             BOARD_LCD_TIMING_HPW,
                             BOARD_LCD_TIMING_HFP);

    // Configure contrast (TODO functions)
    LCD_SetContrastPrescaler(AT91C_LCDC_PS_NOTDIVIDED);
    LCD_SetContrastPolarity(AT91C_LCDC_POL_POSITIVEPULSE);
    LCD_SetContrastValue(0x80);
    LCD_EnableContrast();

    // Configure DMA
    LCD_SetFrameSize(BOARD_LCD_FRAMESIZE);
    LCD_SetBurstLength(4);

    // Set frame buffer
    LCD_SetFrameBufferAddress(primaryBuffer);

    // Enable DMA and LCD
    LCD_EnableDma();
    LCD_Enable(0x0C);
}

//------------------------------------------------------------------------------
/// Callback invoked whenever part of the BMP header has been received. Note
/// that part of the actual image data can be received along the header.
/// \param image  Index of image being received.
/// \param status  Operation result code.
/// \param received  Number of bytes received.
/// \param remaining  Number of bytes discarded or not received.
//------------------------------------------------------------------------------
static void BmpHeaderReceived(
    unsigned int image,
    unsigned char status,
    unsigned int received,
    unsigned int remaining)
{
    imageSizes[image] += received;

    // Check if a full header has been received
    if (imageSizes[image] >= sizeof(struct BMPHeader)) {

        TRACE_INFO("BMP header received.\n\r");

        // Check header validity
        if (!BMP_IsValid((void *) images[image])) {

            TRACE_WARNING( "BMP image is not valid\n\r");

            // Restart transfer
            imageSizes[image] = 0;
            ReceiveBmpHeader(image);
        }
        else {
        
            ReceiveBmpData(image);
        }
    }
    // Otherwise, restart a read operation
    else {

        ReceiveBmpHeader(image);
    }
}

//------------------------------------------------------------------------------
/// Starts the reception of the BMP header data on the USB.
/// \param image  Index of image being received.
//------------------------------------------------------------------------------
static void ReceiveBmpHeader(unsigned int image)
{
    unsigned char status;

    // Start read
    do {

        status = CDCDSerialDriver_Read(
                    (unsigned char *) (images[image] + imageSizes[image]),
                    BOARD_USB_ENDPOINTS_MAXPACKETSIZE(CDCDSerialDriverDescriptors_DATAOUT),
                    (TransferCallback) BmpHeaderReceived,
                    (void *) image);
    }
    while (status != USBD_STATUS_SUCCESS);

    TRACE_INFO("Ready to receive BMP file #%u.\n\r", image);
}

//------------------------------------------------------------------------------
/// Callback invoked when some BMP image data has been received.
/// \param image  Index of image being received.
/// \param status  Operation result code.
/// \param received  Number of image bytes received.
/// \param remaining  Number of bytes not received or discarded.
//------------------------------------------------------------------------------
static void BmpDataReceived(
    unsigned int image,
    unsigned char status,
    unsigned int received,
    unsigned int remaining)
{
    imageSizes[image] += received;

    // Check if image has been completely received
    if (imageSizes[image] == BMP_GetFileSize((void *) images[image])) {

        TRACE_INFO("BMP image #%u received.\n\r", image);

        // Start new transfer if there are images remaining
        if (numImages < MAXIMAGES) {

            images[image+1] = images[image] + imageSizes[image];
            numImages++;
            ReceiveBmpHeader(numImages);
        }
    }
    // Otherwise, restart a read operation
    else {

        ReceiveBmpData(image);
    }
}

//------------------------------------------------------------------------------
/// Starts the reception of the BMP image data on the USB.
/// \param image  Index of image being received.
//------------------------------------------------------------------------------
static void ReceiveBmpData(unsigned int image)
{
    unsigned char status;

    // Start read
    do {

        status = CDCDSerialDriver_Read(
                    (unsigned char *) (images[image] + imageSizes[image]),
                    BMP_GetFileSize((void *) images[image]) - imageSizes[image],
                    (TransferCallback) BmpDataReceived,
                    (void *) image);
    }
    while (status != USBD_STATUS_SUCCESS);
}

//------------------------------------------------------------------------------
/// Interrupt handler for RTT interrupts.
//------------------------------------------------------------------------------
void RTT_IrqHandler()
{
    static unsigned int currentImage = 0;
    volatile unsigned int status;
    unsigned char *tmp;

    status = RTT_GetStatus(AT91C_BASE_RTTC);

    // Check if RTT has been incremented
    if ((status & AT91C_RTTC_RTTINC) != 0) {

        // If there are no images to display, return
        if (numImages == 0) {

            return;
        }

        // Display back buffer (previously decoded image is inside it)
        LCD_SetFrameBufferAddress(backBuffer);
        tmp = backBuffer;
        backBuffer = primaryBuffer;
        primaryBuffer = tmp;

        // Change current image
        currentImage++;
        if (currentImage >= numImages) {

            currentImage -= numImages;
        }

        // Decode new image
        BMP_Decode((void *) images[currentImage],
                   backBuffer,
                   //BOARD_LCD_WIDTH,
                   240, //use a fixed width to meet pic resolution under project dir
                   //BOARD_LCD_HEIGHT,
                   320, //use a fixed width to meet pic resolution under project dir
                   24);
    }
}

//------------------------------------------------------------------------------
/// Main function
//------------------------------------------------------------------------------
int main()
{   
    unsigned int i;

    TRACE_CONFIGURE(DBGU_STANDARD, 115200, BOARD_MCK);
    printf("-- LCD Slideshow Project %s --\n\r", SOFTPACK_VERSION);
    printf("-- %s\n\r", BOARD_NAME);
    printf("-- Compiled: %s %s --\n\r", __DATE__, __TIME__);

    // If they are present, configure Vbus & Wake-up pins
    PIO_InitializeInterrupts(0);

    // Configure pins
    ConfigurePins();

#if !defined(sdram) && defined(PINS_SDRAM)
    // Initialize SDRAM
    BOARD_ConfigureSdram(BOARD_SDRAM_BUSWIDTH);
#endif
    
#if !defined(ddram) && defined(PINS_DDRAM)
    //configure DDRAM for use
    BOARD_ConfigureDdram(0, BOARD_DDRAM_BUSWIDTH);
#endif

    // Initialize LCD
    memset(primaryBuffer, 0, BOARD_LCD_FRAMESIZE * 4);
    memset(backBuffer, 0, BOARD_LCD_FRAMESIZE * 4);
    InitializeLcd();

    // Reset images
    numImages = 0;
    for (i=0; i < MAXIMAGES; i++) {

        images[i] = 0;
        imageSizes[i] = 0;
    }
    images[0] = FIRSTIMAGEADDRESS;

    // Configure RTT for a 2s tick
    RTT_SetPrescaler(AT91C_BASE_RTTC, 0);
    i = RTT_GetTime(AT91C_BASE_RTTC);
    while(i == RTT_GetTime(AT91C_BASE_RTTC));
    IRQ_ConfigureIT(AT91C_ID_SYS, 0, RTT_IrqHandler);
    IRQ_EnableIT(AT91C_ID_SYS);
    RTT_EnableIT(AT91C_BASE_RTTC, AT91C_RTTC_RTTINCIEN);

    // Initialize the serial driver
    CDCDSerialDriver_Initialize();

    // connect if needed
    VBUS_CONFIGURE();

    while (USBD_GetState() < USBD_STATE_CONFIGURED);

    // Infinite loop
    while (1) {

        // Start receiving data on the USB
        if (numImages < MAXIMAGES) {
        
            ReceiveBmpHeader(numImages);
        }
    }
}

