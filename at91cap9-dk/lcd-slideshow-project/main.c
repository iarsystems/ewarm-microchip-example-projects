//------------------------------------------------------------------------------
/// \dir "LCD Slideshow Project"
///
/// !!!Purpose
///
/// This application displays a slideshow on the on-board LCD screen. 
/// Images (in BMP format) can be added to the slideshow by sending them through 
/// a USB virtual COM port created by the program. 
///
/// !See
/// - lcd: LCD driver
/// - rtt: Real time timer driver
/// - usb: USB Framework, USB CDC driver and UDP interface driver
///    - "AT91 USB device framework"
///       - "USBD API"
///    - "cdc-serial"
///
/// !!!Requirements
///
/// This package can be used with all Atmel evaluation kits that have both
/// LCD and UDP interface.
///
/// !!!Description
///
/// When executed, the application waits for incoming BMP files over the USB. 
/// When at least one file has been received, a slideshow animation begins. 
/// It shows each image during two seconds before switching to the next one. 
///
/// This example uses the USB to create a virtual COM port on the host. 
/// This enables easy transfer of image files, since any terminal application
/// can be used for that. #Images must be transferred as a raw stream of data,
/// i.e. without using any protocol such as XMODEM.#
///
/// !!!Usage
///
/// -# Build the application. 
/// -# Optionally, the DBGU port of the board can be connected to the PC and 
/// opened using a terminal application. Debug traces generated by the software
/// will be available that way. 
///     - Settings: 115200 bauds, 8 bits, 1 stop bit, no parity, no flow control. 
/// -# Download the application inside the microcontroller memory and run it. 
/// Please refer to the 
///    <a href="http://www.atmel.com/dyn/resources/prod_documents/doc6224.pdf">
///    SAM-BA User Guide</a>, the 
///    <a href="http://www.atmel.com/dyn/resources/prod_documents/doc6310.pdf">
///    GNU-Based Software Development</a>, application note or to the 
///    <a href="http://www.iar.se/website1/1.0.1.0/78/1/index.php?">
///    IAR EWARM User Guide</a>, depending on your chosen solution. 
/// -# The following message should be displayed on the DBGU: 
///     \code
///     -- LCD Slideshow Project xxx --
///     -- AT91xxxxxx-xx
///     -- Compiled: xxx xx xxxx xx:xx:xx --
///     \endcode
/// -# Connect the USB cable between the evaluation board and the computer. On
/// Microsoft Windows, a driver may be required when the device is plugged for
/// the first time. Point the installation wizard to the 
/// at91lib/usb/device/cdc-serial/drv directory and select the .inf file  
/// located inside it.
/// -# After the device is enumerated properly by the host, a virtual COM port
/// will be created. The application will then wait for BMP files to be sent.
/// This is notified by the message below: 
///        \code
///        Ready to receive BMP file #0. 
///        \endcode
/// -# Open the virtual COM port using a terminal application. 
/// -# Send a BMP image using the terminal to add it to the current slideshow. 
/// The BMP must have the following format: 
///     - Width: 240 pixels 
///     - Height: 320 pixels 
///     - Color resolution: 8- or 24-bits 
/// \n
/// \n
/// #Note 1#: depending on the terminal application, it may be necessary to  
/// select a "binary" transfer mode for the correct data to be sent. 
/// \n
/// #Note 2#: several terminal applications, such as HyperTerminal, use a very 
/// inefficient packet size when transferring data. This may result in a slow 
/// data rate.
///
//------------------------------------------------------------------------------

